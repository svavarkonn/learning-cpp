#include <iostream>
using namespace std;

int main()
{
    int a, b;
    cout << "Enter a number: ";
    cin >> a;
    cout << "Enter another number: ";
    cin >> b;
    cout << "Their sum is " << a + b << endl;
    cout << "and their product is " << a * b << endl;
    cout << "This is the end of the program.\n";
    return 0;
}
